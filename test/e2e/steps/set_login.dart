import 'package:gherkin/gherkin.dart';
import 'package:messenger/domain/model/user.dart';
import 'package:messenger/provider/gql/graphql.dart';

import '../parameters/users.dart';
import '../world/custom_world.dart';

/// Sets the [UserLogin] of the provided [TestUser] to the generated value.
///
/// The value is generated by using `lgn_` prefix and [UserNum.val] and hence is
/// unique among all testing sessions.
final StepDefinitionGeneric setLogin = then1<TestUser, CustomWorld>(
  RegExp(r'{user} sets (?:his|her) login'),
  (TestUser user, context) async {
    final provider = GraphQlProvider();
    final CustomUser? customUser = context.world.sessions[user.name];
    provider.token = customUser?.token;
    final String newLogin = 'lgn_${customUser?.userNum.val}';
    await provider.updateUserLogin(UserLogin(newLogin));
    provider.disconnect();
  },
  configuration: StepDefinitionConfiguration()
    ..timeout = const Duration(minutes: 5),
);
