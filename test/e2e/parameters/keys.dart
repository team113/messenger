/// [CustomParameter] of [WidgetKey]s representing a [Key] of a [Widget].
class WidgetKeyParameter extends CustomParameter<WidgetKey> {
  WidgetKeyParameter()
    : super(
        'key',
        RegExp(
          '`(${WidgetKey.values.map((e) => e.name).join('|')})`',
          caseSensitive: true,
        ),
        (c) => WidgetKey.values.firstWhere((e) => e.name == c),
      );
}
